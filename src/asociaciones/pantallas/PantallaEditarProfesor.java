    public void guardar(Profesor profesor){
        
        Profesor[] profesores=cursillo.getProfesores();
        Curso cursoRecorrido;
        Profesor recorrerProfesores;
        List<Curso> cursos;
        
        
        int tam = profesores.length;
        int cantCursos=Curso.listaCursos.size();
        
        
        
        for(int x=0;x<cantCursos;x++){
            cursoRecorrido=Curso.listaCursos.get(x);
            for(int y=0;y<2;y++){
                recorrerProfesores=cursoRecorrido.getProfesores()[y];
                
                if(recorrerProfesores!=null&&
                        recorrerProfesores.getNombre()==profesor.getNombre()){
                    cursos=recorrerProfesores.getCursos();
                    cursos.add(cursillo);
                    recorrerProfesores.setCursos(cursos);
                    profesor=recorrerProfesores;
                }
                else{
                    cursos =new ArrayList<Curso>();
                    cursos.add(cursillo);
                    profesor.setCursos(cursos);
                }
            }
        }
        for (int i = 0; i < tam; i++) {
           
            
            if(profesores[i]==null){
                profesores[i]=profesor;
                return;
            }
            
            
        }
        JOptionPane.showMessageDialog(this, "Mensaje \n "
                + "Se guardÃ³ correctamente");
        cursillo.setProfesores(profesores);
    }
